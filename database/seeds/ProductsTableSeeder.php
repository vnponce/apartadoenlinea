    /**
     * Run the database seeds.
     *
     * @return void
     */
    public function run()
    {
        // I need to export local products table from DB cuz it does not find products .xlsx
//        Excel::import(new ProductsImport, './productsAll.xlsx');
//        Excel::import(new ProductsImport, './products.xlsx');
        collect($this->products)->map(function($current) {
            $product = collect($current);
            Product::create([
                "name" => $product->get('name'),
                "description" => $product->get('description'),
                "ingredients" => $product->get('ingredients'),
                // "image" => $product->get('image'),
                "image" => $this->getImagePath($product->get('image')),
                "price" => $product->get('price') / 100,
                "available" => $product->get('available'),
                "available_time" => $product->get('available_time'),
                "favorite" => $product->get('favorite'),
                "category_id" => $product->get('category_id'),
            ]);
        });
    }
